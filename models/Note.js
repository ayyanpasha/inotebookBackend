const mongoose = require("mongoose");
const { Schema } = require("mongoose");

const NoteSchema = new Schema({
    user: {
        type: Schema.Types.ObjectId,
        ref: 'User'
    },
    title: {
        type: String,
        required: true
    },
    description: {
        type: String,
        required: true
    },
    tag: {
        type: String,
        default: "General"
    },
    date: {
        type: Date,
        default: Date.now
    }
});

const Note = mongoose.model('Note', NoteSchema);
Note.createIndexes();
module.exports = Note;